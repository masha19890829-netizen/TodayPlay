    fun search(cityOrQuery: String?, interests: List<String>, relationship: String): List<POI> {
        val normalizedQuery = cityOrQuery.orEmpty().trim().lowercase()
        val cityMatched = pois.filter { poi ->
            normalizedQuery.isBlank() ||
                poi.city.lowercase().contains(normalizedQuery) ||
                poi.country.lowercase().contains(normalizedQuery) ||
                poi.name.lowercase().contains(normalizedQuery) ||
                poi.tags.any { it.lowercase().contains(normalizedQuery) }
        }
        val base = cityMatched.ifEmpty { pois }
        return base.sortedWith(
            compareByDescending<POI> { poi ->
                val relationshipScore = if (relationship in poi.suitableRelationships || "all" in poi.suitableRelationships) 4 else 0
                val interestScore = poi.tags.count { tag -> tag in interests }
                relationshipScore + interestScore
            }.thenBy { poi -> poi.estimatedStayMinutes },
        )
    }

