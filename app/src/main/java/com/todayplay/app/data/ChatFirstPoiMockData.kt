    fun search(cityOrQuery: String?, interests: List<String>, relationship: String): List<POI> {
        val query = cityOrQuery.orEmpty().trim().lowercase()
        val base = all.filter { poi ->
            query.isBlank() ||
                poi.city.lowercase().contains(query) ||
                poi.name.lowercase().contains(query) ||
                poi.district.lowercase().contains(query)
        }.ifEmpty { all }
        return base.sortedWith(
            compareByDescending<POI> { poi ->
                val relationshipScore = if (relationship in poi.suitableRelationships || "all" in poi.suitableRelationships) 6 else 0
                val interestScore = poi.tags.count { tag -> tag in interests || interests.any { interest -> tag.contains(interest) || interest.contains(tag) } } * 2
                relationshipScore + interestScore
            }.thenBy { poi -> poi.estimatedStayMinutes },
        )
    }

